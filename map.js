let map;
async function initMap() {
const startPosition = {
      lat: 43.85006694816966,
      lng: 18.407905721139482,
};
    var map = new google.maps.Map(document.getElementById("map"), {
      center: startPosition,
      zoom: 7,
    });
    let infowindow = new google.maps.InfoWindow();
    for(let i=0;i<bhLocations.length;i++){
    let marker = new google.maps.Marker({
      position: {
        lat: bhLocations[i].lat,
        lng: bhLocations[i].lng,
  },
      map,
      name: bhLocations[i].name,
      tags: bhLocations[i].tags,
    });
    
    google.maps.event.addListener(marker, 'click', (function(marker, i) {
      return function() {
        let infoWindowNode = document.createElement('div');
        infowindow.setContent(bhLocations[i].name);
        let infoImg = '<h4 style="margin-block-start: 0;">' + bhLocations[i].name + '</h4>';
        infoImg += '<p style="font-style: italic;">' + bhLocations[i].tags.join(" - ") + '</p>';
        infoImg += '<img style="width: 100%;  margin: auto; display: block; margin-left: auto; margin-right: auto;" src="' + bhLocations[i].imgUri + '" />';
        let textNode = document.createElement('div');      
        textNode.innerHTML = infoImg;
        infoWindowNode.append(textNode);
        infowindow.setContent(infoWindowNode);
        infowindow.open(map, marker);
      }
    })(marker, i));
  };
}

